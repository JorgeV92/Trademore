# helper functions for creating the training and test Datasets that emit words

class StockDataset(Dataset):

    def __init__(self, stock_data, features):
        self.stock_data = stock_data
        self.features = features
        self.max_len = max(len(d) for d in self.stock_data)

    def __len__(self):
        return len(self.stock_data)

    def __getitem__(self, idx):
        data = self.stock_data[idx]
        # print(f"Data before conversion to tensor: {data}") # Debugging Test ...
        x = torch.zeros(self.max_len, dtype=torch.float)
        y = torch.zeros(self.max_len, dtype=torch.float)
        x[:len(data)] = torch.tensor(data[:-1], dtype=torch.float)
        y[:len(data)-1] = torch.tensor(data[1:], dtype=torch.float)
        return x, y
    
    def get_sequence_length(self):
        # the sequence length will be the maximum length of any sequence in your dataset
        return self.max_len
    
    def get_num_features(self):
        # the number of features is the length of the features list you pass to the constructor
        return len(self.features)

def create_datasets(input_file):
    # preprocessing of the input csv file
    stock_data = pd.read_csv(input_file)
    stock_data = stock_data.fillna(0)
    features = stock_data.columns.drop('Date').tolist()
    stock_data = stock_data[features].values.tolist()

    # partition the input data into a training and the test set
    test_set_size = min(1000, int(len(stock_data) * 0.1)) # 10% of the training set, or up to 1000 examples
    rp = torch.randperm(len(stock_data)).tolist()
    train_data = [stock_data[i] for i in rp[:-test_set_size]]
    test_data = [stock_data[i] for i in rp[-test_set_size:]]

    # wrap in dataset objects
    train_dataset = StockDataset(train_data, features)
    test_dataset = StockDataset(test_data, features)

    return train_dataset, test_dataset

class InfiniteDataLoader:
    """
    this is really hacky and I'm not proud of it, but there doesn't seem to be
    a better way in PyTorch to just create an infinite dataloader?
    """

    def __init__(self, dataset, **kwargs):
        train_sampler = torch.utils.data.RandomSampler(dataset, replacement=True, num_samples=int(1e10))
        self.train_loader = DataLoader(dataset, sampler=train_sampler, **kwargs)
        self.data_iter = iter(self.train_loader)

    def next(self):
        try:
            batch = next(self.data_iter)
        except StopIteration: # this will technically only happen after 1e10 samples... (i.e. basically never)
            self.data_iter = iter(self.train_loader)
            batch = next(self.data_iter)
        return batch

